package com.example.phakjira.namjai;

public class RandomNamJai {
    String name;
    String description;

    public RandomNamJai(String name, String description) {
        this.name = name;
        this.description = description;
    }

    public RandomNamJai(){

    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }
}
